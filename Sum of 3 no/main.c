#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a=3; //integer//
    float b=4.5; //floating point numbers//
    double c=5.25; //double is also floating point number//
    float sum;
    sum = a + b + c;
    printf("sum of 3 nos is %f",sum); // %f is for floating point data type//
    return 0;

}

//%s refers to a string data type, %f refers to a float data type, and %d refers to a double data type, integers.//
